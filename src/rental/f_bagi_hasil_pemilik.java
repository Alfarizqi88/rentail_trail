    private void btnexportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexportActionPerformed
        // TODO add your handling code here:
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if(x==JFileChooser.APPROVE_OPTION){path=j.getSelectedFile().getPath();}
        
        Document doc = new Document();
        
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"data_bagi_hasil_pemilik_pemesanan.pdf"));
            doc.open();
            PdfPTable tbl = new PdfPTable(4);
            tbl.addCell("id_motor");
            tbl.addCell("nama_motor");
            tbl.addCell("tanggal sewa");
            tbl.addCell("bagi pemilik");
            
            for(int i=0; i<tbl_list_pesanan.getRowCount(); i++){
                String id_motor=tbl_list_pesanan.getValueAt(i, 0).toString();
                String nama_motor=tbl_list_pesanan.getValueAt(i, 1).toString();
                String tanggal_sewa=tbl_list_pesanan.getValueAt(i, 2).toString();
                String bagi_pemilik=tbl_list_pesanan.getValueAt(i, 3).toString();
                
                tbl.addCell(id_motor);
                tbl.addCell(nama_motor);
                tbl.addCell(tanggal_sewa);
                tbl.addCell(bagi_pemilik);
            }
            doc.add(tbl);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(f_bagi_hasil_pemilik.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(f_bagi_hasil_pemilik.class.getName()).log(Level.SEVERE, null, ex);
        }
        doc.close();
    }//GEN-LAST:event_btnexportActionPerformed

