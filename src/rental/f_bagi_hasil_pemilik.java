    public void cari(String tgl_awal, String tgl_akhir, int id_pemilik) {
        String[] kolom = {"id_pemilik", "nama pemilik", "tanggal awal", "tanggal akhir", "pendapatan"};
        ArrayList<data_bagi_hasil_pemilik> list = new data_bagi_hasil_pemilik().search(tgl_awal, tgl_akhir, id_pemilik);
        Object rowData[] = new Object[5];
        tbl_cek_transaksi.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (data_bagi_hasil_pemilik dct : list) {
            rowData[0] = dct.getId_pemilik();
            rowData[1] = dct.getNama_pemilik();
            rowData[2] = dct.getTgl_awal();
            rowData[3] = dct.getTgl_akhir();
            rowData[4] = dct.getTotal_hasil();
            ;
            ((DefaultTableModel) tbl_cek_transaksi.getModel()).addRow(rowData);
        }

    }
    
     public void cari_list(String tgl_awal, String tgl_akhir, int id_pemilik) {
        String[] kolom = {"id motor", "nama motor", "tanggal sewa", "bagi pemilik"};
        ArrayList<data_bagi_hasil_pemilik> list = new data_bagi_hasil_pemilik().search_list(tgl_awal, tgl_akhir, id_pemilik);
        Object rowData[] = new Object[4];
        tbl_list_pesanan.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (data_bagi_hasil_pemilik dct : list) {
            rowData[0] = dct.getId_motor();
            rowData[1] = dct.getJenis_motor();
            rowData[2] = dct.getTgl_sewa();
            rowData[3] = dct.getBagi_pemilik();
            
            ;
            ((DefaultTableModel) tbl_list_pesanan.getModel()).addRow(rowData);
        }

    }
    
    public void tampilkan_cmb() {
        cmb_pemilik.setModel(new DefaultComboBoxModel(new data_pemilik().getAll().toArray()));
        cmb_pemilik.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    //String a = combo_plat.getSelectedItem().toString();
                    String data = cmb_pemilik.getSelectedItem().toString();
                    String[] sp1 = data.split("\\|");
                    String[] sp2 = sp1[0].split("\\:");

                    txt_id_pemilik.setText(sp2[1]);

                    //System.out.println(a);
                }
            }
        });
    }
