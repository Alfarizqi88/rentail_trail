    public void tampilkanData() {
        String[] kolom = {"id_customer","nama_customer", "no_telepon","alamat"};
        ArrayList<data_customer> list = new data_customer().getAll();
        Object rowData[] = new Object[4];
        tblcustomer.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (data_customer dm : list) {
            rowData[0] = dm.getId_customer();
            rowData[1] = dm.getNama_customer();
            rowData[2] = dm.getNo_telepon();
            rowData[3] = dm.getAlamat();
            ((DefaultTableModel) tblcustomer.getModel()).addRow(rowData);
        }   
    }
    public void kosongkan_form() {
        txtidcustomer.setEnabled(false);
        txtidcustomer.setText("");
        txtnamacustomer.setText("");
        txtnotelepon.setText("");
        txtalamat.setText("");
    }
    
    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        data_customer dc = new data_customer();
        if(txtidcustomer.getText().equals("")){
            txtidcustomer.setText("0");
        }
        if (txtnamacustomer.getText().equals("") || txtnotelepon.getText().equals("")|| txtalamat.getText().equals("") ) {
            JOptionPane.showMessageDialog(this, "inputan kosong");
            
        } else {
            System.out.println("save");
            
            dc.setId_customer(Integer.parseInt(txtidcustomer.getText()));
//            JOptionPane.showMessageDialog(null, id_customer);
            dc.setNama_customer(txtnamacustomer.getText());
            dc.setNo_telepon(txtnotelepon.getText());
            dc.setAlamat(txtalamat.getText());
            dc.save_data();
            tampilkanData();
        }
        
    }//GEN-LAST:event_btnsaveActionPerformed

