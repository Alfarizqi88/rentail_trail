    public void cari(String keyword) {
        String[] kolom = {"id_customer","nama_customer", "no_telepon","alamat"};
        ArrayList<data_customer> list = new data_customer().search(keyword);
        Object rowData[] = new Object[4];
        tblcustomer.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (data_customer dm : list) {
            rowData[0] = dm.getId_customer();
            rowData[1] = dm.getNama_customer();
            rowData[2] = dm.getNo_telepon();
            rowData[3] = dm.getAlamat();
            ((DefaultTableModel) tblcustomer.getModel()).addRow(rowData);
        }
    }
