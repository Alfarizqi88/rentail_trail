    public void cari(String tgl_awal, String tgl_akhir) {
        String[] kolom = { "tanggal awal", "tanggal akhir", "pendapatan"};
        ArrayList<data_bagi_hasil_rental> list = new data_bagi_hasil_rental().search(tgl_awal, tgl_akhir);
        Object rowData[] = new Object[3];
        tbl_hasil_rental.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (data_bagi_hasil_rental dct : list) {
            rowData[0] = dct.getTgl_awal();
            rowData[1] = dct.getTgl_akhir();
            rowData[2] = dct.getTotal();
            ((DefaultTableModel) tbl_hasil_rental.getModel()).addRow(rowData);
        }

    }

