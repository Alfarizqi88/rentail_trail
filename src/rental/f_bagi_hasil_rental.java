    private void btnexportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexportActionPerformed
        // TODO add your handling code here:
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if(x==JFileChooser.APPROVE_OPTION){path=j.getSelectedFile().getPath();}
        
        Document doc = new Document();
        
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"data_bagi_hasil_rental.pdf"));
            doc.open();
            PdfPTable tbl = new PdfPTable(3);
            tbl.addCell("Tanggal Awal");
            tbl.addCell("Tanggal Akhir");
            tbl.addCell("Pendapatan");
            
            for(int i=0; i<tbl_hasil_rental.getRowCount(); i++){
                String tgl_awal=tbl_hasil_rental.getValueAt(i, 0).toString();
                String tgl_akhir=tbl_hasil_rental.getValueAt(i, 1).toString();
                String pendapatan=tbl_hasil_rental.getValueAt(i, 2).toString();
                
                tbl.addCell(tgl_awal);
                tbl.addCell(tgl_akhir);
                tbl.addCell(pendapatan);
            }
            doc.add(tbl);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(f_bagi_hasil_pemilik.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(f_bagi_hasil_pemilik.class.getName()).log(Level.SEVERE, null, ex);
        }
        doc.close();
    }//GEN-LAST:event_btnexportActionPerformed

