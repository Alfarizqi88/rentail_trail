    private void txtbayardendaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbayardendaKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            int x = Integer.parseInt(txtbayardenda.getText());
            int y = Integer.parseInt(txtdenda.getText());
            int hasil = x-y;
            if(x < y){
                JOptionPane.showMessageDialog(this, "mohon lakukan pembayaran");
            }else{
                txtkembali.setText(Integer.toString(hasil));
                btnsave.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_txtbayardendaKeyPressed

