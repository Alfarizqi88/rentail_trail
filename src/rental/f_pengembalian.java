    public void cari(String nama, String tgl_sewa) {
        String[] kolom = {"id transaksi", "id_detail", "nama customer", "tanggal sewa", "tanggal kembali", "jenis motor", "jaminan", "tanggal pengembalian", "denda"};
        ArrayList<data_pengembalian_motor> list = new data_pengembalian_motor().search(nama, tgl_sewa);
        Object rowData[] = new Object[9];
        tbl_pengembalian.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        for (data_pengembalian_motor dpm : list) {
            rowData[0] = dpm.getId_transaksi();
            rowData[1] = dpm.getId_detail();
            rowData[2] = dpm.getNama_customer();
            rowData[3] = dpm.getTgl_sewa();
            rowData[4] = dpm.getTgl_kembali();
            rowData[5] = dpm.getJenis_motor();
            rowData[6] = dpm.getJaminan();
            rowData[7] = dpm.getTgl_pengembalian();
            rowData[8] = dpm.getDenda();
            ;
            ((DefaultTableModel) tbl_pengembalian.getModel()).addRow(rowData);
        }

    }
    
    public void save(){
        if (txtdenda.getText().equals("")|| txtbayardenda.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mohon Diisi");
        } else {
            data_pengembalian_motor dpm = new data_pengembalian_motor();
            dpm.setId_detail(Integer.parseInt(txt_id_detail.getText()));
            dpm.setId_transaksi(Integer.parseInt(txt_id_transaksi.getText()));
            dpm.setStatus("available");
            dpm.setDenda(Integer.parseInt(txtdenda.getText()));
            dpm.save_data();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String nama_customer = txt_id_transaksi.getText();
            cari(nama_customer, sdf.format(tgl_sewa.getDate()));
            cari(nama_customer, sdf.format(tgl_sewa.getDate()));
            JOptionPane.showMessageDialog(this, "Sukses");
        }
    }
